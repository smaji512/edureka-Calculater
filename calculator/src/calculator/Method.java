package calculator;

public class Method {
	public double first, second, total;

	public double Addition(double fnum, double snum) {

		total = fnum + snum;

		return total;

	}

	public double Division(double fnum, double snum) {

		total = fnum / snum;
		return total;
	}

	public double Multipication(double fnum, double snum) {

		total = fnum * snum;
		return total;
	}

	public double Subtraction(double fnum, double snum) {

		total = fnum - snum;

		return total;

	}

}
